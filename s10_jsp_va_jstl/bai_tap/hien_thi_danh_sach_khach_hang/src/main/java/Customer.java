public class Customer {
    private String name;
    private int gender;
    private String dOB;
    private String address;
    private String img;

    public Customer() {
    }

    public Customer(String name, int gender, String dOB, String address, String img) {
        this.name = name;
        this.gender = gender;
        this.dOB = dOB;
        this.address = address;
        this.img = img;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getGender() {
        return gender;
    }

    public void setGender(int gender) {
        this.gender = gender;
    }

    public String getdOB() {
        return dOB;
    }

    public void setdOB(String dOB) {
        this.dOB = dOB;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }
}
